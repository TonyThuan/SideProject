uint32_t get_choose(uint8_t num_file_folder)
{
    uint32_t choose;

    do
    {
        printf("\nYou choose: ");
        scanf("%d", &choose);
        fflush(stdin);
        printf("\n");
    }
    while((choose < 0) || choose > num_file_folder);

    return choose;
}
